let num = prompt("Enter the number ?");
num = Number.parseInt(num);
switch (num) {
  case 12:
    console.log("Number is 12");
    break;
  case 13:
    console.log("Number is 13");
    break;
  case 14:
    console.log("Number is 14");
    break;
  case 15:
    console.log("Number is 15");
    break;
  case 16:
    console.log("Number is 16");
    break;
  default:
    console.log("Number is none of the above");
    break;
}
